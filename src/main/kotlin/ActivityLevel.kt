/**
 * Enum class for activity levels
 * @property ABSENT student is not present
 * @property INATTENTIVE student is present and actively not paying attention
 * @property PASSIVE student is present and passively paying attention
 * @property ACTIVE student is present and participating in some way
 * @property VERY_ACTIVE student is present and participating in a very active way
 */
enum class ActivityLevel {
    ABSENT,
    INATTENTIVE,
    PASSIVE,
    ACTIVE,
    VERY_ACTIVE
}